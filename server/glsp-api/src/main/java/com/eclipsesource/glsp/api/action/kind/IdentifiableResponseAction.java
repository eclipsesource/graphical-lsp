package com.eclipsesource.glsp.api.action.kind;

import com.eclipsesource.glsp.api.action.Action;

public class IdentifiableResponseAction extends Action {
	private String id;
	private Action action;

	public IdentifiableResponseAction() {
		super(Action.Kind.IDENTIFIABLE_RESPONSE_ACTION);
	}

	public IdentifiableResponseAction(String id, Action action) {
		this();
		this.id = id;
		this.action = action;
	}

	public String getId() {
		return id;
	}

	public Action getAction() {
		return action;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = super.hashCode();
		result = prime * result + ((action == null) ? 0 : action.hashCode());
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (!super.equals(obj)) {
			return false;
		}
		if (!(obj instanceof IdentifiableResponseAction)) {
			return false;
		}
		IdentifiableResponseAction other = (IdentifiableResponseAction) obj;
		if (action == null) {
			if (other.action != null) {
				return false;
			}
		} else if (!action.equals(other.action)) {
			return false;
		}
		if (id == null) {
			if (other.id != null) {
				return false;
			}
		} else if (!id.equals(other.id)) {
			return false;
		}
		return true;
	}

}
